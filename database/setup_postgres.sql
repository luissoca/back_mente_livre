CREATE INDEX IF NOT EXISTS idx_email_classifications_user ON email_classifications(user_id);
COMMENT ON TABLE email_classifications IS 'Clasificación de emails por dominio';

DROP TRIGGER IF EXISTS update_email_classifications_updated_at ON email_classifications;
CREATE TRIGGER update_email_classifications_updated_at BEFORE UPDATE ON email_classifications FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

