-- Citas
CREATE TABLE IF NOT EXISTS appointments (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  therapist_id UUID NOT NULL REFERENCES therapists(id) ON DELETE CASCADE,
  user_id UUID REFERENCES users(id) ON DELETE SET NULL,
  patient_contact_id UUID REFERENCES patient_contacts(id) ON DELETE SET NULL,
  patient_email VARCHAR(255) NOT NULL,
  patient_name VARCHAR(200) NOT NULL,
  patient_phone VARCHAR(20),
  consultation_reason TEXT,
  appointment_date DATE NOT NULL,
  start_time TIME NOT NULL,
  end_time TIME NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'completed', 'cancelled', 'pending_payment', 'payment_review')),
  pricing_tier TEXT CHECK (pricing_tier IN ('university_pe', 'university_international', 'corporate', 'public')),
  promo_code_id UUID REFERENCES promo_codes(id) ON DELETE SET NULL,
  email_used VARCHAR(255),
  notes TEXT,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_appointment_slot UNIQUE (therapist_id, appointment_date, start_time)
);
CREATE INDEX IF NOT EXISTS idx_appointments_therapist ON appointments(therapist_id);
CREATE INDEX IF NOT EXISTS idx_appointments_user ON appointments(user_id);
CREATE INDEX IF NOT EXISTS idx_appointments_date ON appointments(appointment_date);
CREATE INDEX IF NOT EXISTS idx_appointments_status ON appointments(status);
CREATE INDEX IF NOT EXISTS idx_appointments_patient_email ON appointments(patient_email);
CREATE INDEX IF NOT EXISTS idx_appointments_promo_code ON appointments(promo_code_id);
COMMENT ON TABLE appointments IS 'Citas/agendamientos';

DROP TRIGGER IF EXISTS update_appointments_updated_at ON appointments;
CREATE TRIGGER update_appointments_updated_at BEFORE UPDATE ON appointments FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

